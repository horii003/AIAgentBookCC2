"""経費精算申請ビジネスルール・ポリシー

AG-003（経費精算申請エージェント）が使用するルールテキストを提供する。
"""


def get_expense_rules(
    deadline_months: int = 3,
    approval_threshold: int = 5000,
) -> str:
    """経費精算申請に関するビジネスルールテキストを取得する。

    Args:
        deadline_months: 申請期限（経費発生日からの月数）
        approval_threshold: 上長承認が必要な経費合計閾値（円）

    Returns:
        str: 業務ルールテキスト（マークダウン形式）
    """
    return f"""## 経費精算申請ルール

### 申請期限
- 経費発生日（購入日）から{deadline_months}ヶ月以内に申請すること（BRL-14）
- 申請期限を超過している場合は社員に通知するが、申請フローは継続可能

### 上長承認要否
- 経費合計が{approval_threshold:,}円を超える場合は上長の事前承認が必要（BRL-13）
- 合計が{approval_threshold:,}円以下の場合は上長承認不要

### 経費区分
経費は以下の4区分に分類する（BRL-12）:

| 経費区分 | 対象品目例 |
|---------|----------|
| 事務用品費 | 文房具・コピー用紙・封筒・印刷消耗品等の事務用消耗品 |
| 宿泊費 | 出張時のホテル宿泊・旅館等の宿泊施設利用料 |
| 資格精算費 | 業務関連資格の受験料・参考書・セミナー受講料 |
| その他経費 | 上記に分類できない業務関連経費 |

- 品目から経費区分を判断できない場合は「その他経費」を候補として社員に確認を促す（COND-002）

### 申請書記載ルール（BRL-04）
- 購入日・店舗名・品目・経費区分・金額・業務目的を正確に記載する
- 収集済み情報に存在しないフィールドを推測・補完してはならない

### 申請書の生成
- 社員の承認（HitL承認）後のみ申請書を生成する（GRD-009）
- 申請書の自動送信・実際の提出は禁止（GRD-010）
- 交通費計算ツール（TOOL-001）は使用禁止
"""
