"""交通費精算申請ビジネスルール・ポリシー

AG-002（交通費精算申請エージェント）が使用するルールテキストを提供する。
"""


def get_transport_rules(
    deadline_months: int = 3,
    approval_threshold: int = 10000,
) -> str:
    """交通費精算申請に関するビジネスルールテキストを取得する。

    Args:
        deadline_months: 申請期限（移動日からの月数）
        approval_threshold: 上長承認が必要な交通費合計閾値（円）

    Returns:
        str: 業務ルールテキスト（マークダウン形式）
    """
    return f"""## 交通費精算申請ルール

### 申請期限
- 移動日から{deadline_months}ヶ月以内に申請すること（BRL-14）
- 申請期限を超過している場合は社員に通知するが、申請フローは継続可能

### 上長承認要否
- 交通費合計が{approval_threshold:,}円を超える場合は上長の事前承認が必要（BRL-09）
- 合計が{approval_threshold:,}円以下の場合は上長承認不要

### 申請対象となる交通費
- 業務目的の移動（出張・顧客訪問・社内移動等）の交通費
- 電車・バス・タクシー・飛行機等の公共交通機関および交通手段

### 申請書記載ルール（BRL-04/BRL-07）
- 移動日・出発地・目的地・交通手段・業務目的を正確に記載する
- 駅名は末尾の「駅」を除去して正規化する（BRL-10）
- 収集済み情報に存在しないフィールドを推測・補完してはならない

### 申請書の生成
- 社員の承認（HitL承認）後のみ申請書を生成する（GRD-009）
- 申請書の自動送信・実際の提出は禁止（GRD-010）
"""
